<p></p><font face="georgia" size="2"><p>The name "Horsley" is believed to be derrived from the pre 7th century Old 
English phrase "horse-lega", which means the "place of the horses". This may 
refer to a field of horses, a horse racing track, or a training ground. 
<br><br>The Parish is situated on part of the Great Cotswold ridgeway, which was 
possibly used by the Romans, as a Roman route branched south-westwards from it 
at Chavenage Green. The green was the meetingplace of the hundred court and was 
where the Earl Godwin assembled an army against Edward the Confessor in 1051.&nbsp;<br></p><p><br></p></font>
