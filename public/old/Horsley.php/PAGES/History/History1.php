<font face="georgia" size="2">In 1327 49 inhabitants were assessed for tax. The parish was apparently severely  depopulated at the time of the Black Death but in 1381 62 persons were assessed  for tax. In 1551 there were 217 communicants in the parish and 56 households  were recorded in 1563. The number of communicants had increased to 400 by 1603  and there were said to be 200 families in 1650. In the early 18th century there  were said to be <i>c</i>. 1,200 people living in 300 houses in the parish. The  population increased from 2,971 in 1801 to a peak of 3,690 in 1831 but the  decline of the cloth industry resulted in emigration, some of it officially  sponsored, and in 1861 2,558 people were enumerated. <br><br>A parliamentary  garrison was stationed at Horsley in 1643. Henry Sheppard, a prominent lawyer on  the parliamentary side, was born in the parish. In the first decade of the 19th  century a troop of volunteer infantry was raised at Horsley and presumably  included men from the surrounding parishes. <br></font>
