<p></p><font face="georgia" size="2"><p>Downend, a small settlement north of Horsley village, includes a pair of 
17th-century gabled houses. The earlier, western house was formerly the White 
Hart inn, recorded from 1798. The situation of the houses, with a small 
19th-century residence to the north, suggests that there may have once been a 
mill there on a tributary of the Horsley stream. North of Downend are Sugley 
Farm, a 17th-century farm-house greatly enlarged in the 19th century, and 
Tickmorend, where a house was recorded in 1660. </p><p>&nbsp;</p><p>In 1755 four licensed premises were recorded in Horsley parish, including  that part later transferred to Nailsworth. The same area had ten ale-houses in  1785 but the magistrates reduced their number to three the following year. In  1838, however, Horsley had ten public houses and 31 beer-houses, most of which  were presumably in the area later transferred to Nailsworth.&nbsp;<br>&nbsp;</p></font>
