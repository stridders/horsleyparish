<p><span style="font-family: georgia; font-size: small;">London way was crossed there by the old road from Horsley to Tetbury by way of Hartley bridge and Tiltups End, and by the old Nailsworth-Tetbury road through Windsorash and Ledgemore Bottom.</span><span style="font-family: georgia; font-size: small;">&nbsp;</span><font face="georgia" size="2">The valley sides are known to have supported extensive beech woods, for which 
the manor employed a woodward in 1293. </font></p><p><font face="georgia" size="2">&nbsp;</font></p><p><font face="georgia" size="2">The lord's wood at Lutheredge, on the north-west boundary of the parish, was 
mentioned in 1530, but most of the woodland was apparently open to the commoning 
rights of the inhabitants until 1655 when the lord of the manor was permitted by 
the parishioners, in return for an annual rent, to inclose over 230 a. of 
woodland at Longridge, Winnowshedge, and Sealy wood. Another wood, called 
Shortwood, lying south of the hamlet later transferred to Nailsworth, was a 
custom wood, in which the inhabitants had both common and the right to take 
timber; it was put under the control of trustees for the inhabitants in 1655. It 
covered 105 a. in 1733, but was felled <i>c</i>. 1829. <br><br>The main 
Bath-Gloucester road, bisecting the parish from south to north, was built in 
1780, although the stretch up to Tiltups End had existed as a turnpike from 
1758. The Nailsworth-Dursley road through Horsley village was turnpiked between 
Nailsworth and Latterwood in Owlpen in 1800. <br><br>A church, presumably 
occupying the site of the parish church, was recorded at Horsley in 1105 and a 
priory was built to the south of the site. The priory, whose buildings were in a 
ruinous state in 1375, had closed by 1380; an old chapel and a gateway were 
still standing in the early 18th century. In addition to a settlement near the 
church and priory there was a settlement at Ledgemore Bottom in the east part of 
the parish, presumably served by the church recorded at Chavenage in the mid 
13th century. Eleven inhabitants were assessed for tax at Ledgemore in 1327, but 
the village was deserted in 1381, probably as a result of the Black Death which 
is known to have caused many deaths in the parish. <br><br>The medieval 
manor-house was built at Chavenage, and a farm-house recorded in the mid 16th 
century, when it contained a hall and a parlour with lofts above, a kitchen, and 
a white house, was probably that known as Manor Farm north of the manorhouse. 
The farm-house dates from the 16th century and had a north wing added in the 
17th. Lodge Farm, east of the manor-house, was built during the 18th 
century.<br><br>Horsley village developed from the cross-roads east of the 
church where a group of buildings, including the former Boot inn, recorded 
between 1779 and 1939, retain features dating from the late 17th or early 18th 
century. Tradition maintains that the cross-roads was the site of the market. 
North of the cross-roads stands Horsley Court, a house predominantly of the 
early 19th century but incorporating a staircase and part of the structure of an 
early-18th-century house. The large room occupying the north half of the main 
front was formerly of double height with an encircling gallery at first-floor 
level. A three-storey porch and a north-west service wing were added in the 19th 
century. During the later 19th century it was the chief house of an estate of 
<i>c</i>. 100 a. owned by Edward Wood Mason (d. 1883). Cottages were built east 
of the village on Hay Lane and the lane leading to Washpool from the 18th 
century. <br><br>The site of the priory was used for a house of correction, 
opened in 1791 and built according to the principles of Sir George Onesiphorous 
Paul. The prison was closed and sold in 1878 when the site contained a petty 
sessional court and committee rooms, cell blocks, chapel, infirmary, two 
dwellinghouses, and a lodge. Most of the buildings were demolished but a 
three-storeyed block was converted for use as a residence, and in 1972, called 
the Priory, was used as a local office by the highways department of 
Gloucestershire county council. A small council estate was built near by in the 
mid 20th century. In the late 18th and 19th centuries the village grew westwards 
along the main street to Nupend where a church house was recorded in 1671. Two 
farm-houses, one of which was used as cottages in 1972, and a cottage at the 
west end of Nupend date from the 17th century but most of the buildings are of 
the 18th or 19th century. <br><br>A habitation was recorded in 1327 at Barton 
End, named from a barton on the manor estate. Some small houses at Upper Barton 
End date from the late 17th or early 18th century as does a gabled house, used 
as a small hotel in 1972, situated at Tiltups End Â½ mile south of Barton End. 
An inn, called Tiltups inn or the Black Horse, was recorded there from 1769 and 
was housed in a mid-19th-century building in 1972.<br></font></p><p><font face="georgia" size="2"><br></font></p>
